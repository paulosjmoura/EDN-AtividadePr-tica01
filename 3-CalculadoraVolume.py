'''3- Calculadora de Volume
* Crie um programa que calcula o volume de uma caixa retangular. Use as seguintes dimensões:

* Comprimento: 12 cm
* Largura: 14 cm
* Altura: 20 cm
O programa deve calcular o volume e exibir o resultado em cm³.'''

comprimento = 12
largura = 14
altura = 20
volume = 0

volume = comprimento * largura * altura

volumeTotal = f"O volume total com as dimensões:  Comprimento: {comprimento} cm, Largura: {largura} cm e Altura: {altura} cm é de: {volume} cm³ "
print(volumeTotal)


